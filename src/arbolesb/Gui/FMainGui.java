    private void bEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bEliminarActionPerformed
        try {
            int val = Integer.parseInt(tElemento.getText());
            
            if (!Main.EstaElemento(val)) {
                JOptionPane.showMessageDialog(this, "Elemento " + val +
                        " no se encuentra en el arbol", "Error al Eliminar",
                        JOptionPane.WARNING_MESSAGE);
                
            } else {
                Main.EliminarElementoEnArbol(val);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Escriba un numero entero",
                    "Error al Eliminar", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_bEliminarActionPerformed

