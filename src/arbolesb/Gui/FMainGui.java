    private void agregarHijos(DefaultMutableTreeNode n) {
        Nodo nodo = (Nodo) n.getUserObject();
        if (nodo == null || nodo.getHijos() == null) {
            return;
        }
        for (Nodo o : nodo.getHijos()) {
            if (o != null) {
                DefaultMutableTreeNode cur = new DefaultMutableTreeNode(o);
                n.add(cur);
                agregarHijos(cur);
            }
        }
    }
